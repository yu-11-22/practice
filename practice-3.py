# 一個整數，它加上36後是一個完全平方數，再加上49又是一個完全平方數，請問該數是多少？
for i in range(1, 50):                           # x+36=n**2  x+36+49=m**2
    if 49 % i == 0:                                 # (m+n)(m-n)=49
        j = 49/i                                  # 設m+n=i m-n=j
        if i > j and (i+j) % 2 == 0 and (i-j) % 2 == 0:
            m = (i+j)/2
            n = (i-j)/2
            x = n**2-36
            print(x)
